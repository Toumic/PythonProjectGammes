# ------------------------------------------------------------
# Mode Streamlit
# ------------------------------------------------------------
def run_streamlit_mode():
    resultat = analyser_gammes(i_grok)
    gammes = resultat["gammes_fondamentales"]

    st.title("DEBUG GAMMES — VERSION PROPRE")

    # Sélecteur
    choix = st.selectbox("Choix :", list(gammes.keys()))

    # Gamme sélectionnée
    g = gammes[choix]
    print("g", g)

    # Affichage propre
    st.subheader("OBJET g (complet)")
    st.write(g)  # Remplace st.json(g)

    st.markdown("---")

    st.subheader("Notes")
    st.write(g["notes"])

    st.subheader("Type")
    st.write(g["type"])

    st.subheader("Signal")
    st.write(g["signal"])

    st.subheader("Forces")
    st.write(g["forces"])

    st.subheader("Effets")
    st.write(g["effets"])


# ------------------------------------------------------------
# Exécution Streamlit
# ------------------------------------------------------------
if st.runtime.exists():
    run_streamlit_mode()

# ------------------------------------------------------------
# Exécution Python classique
